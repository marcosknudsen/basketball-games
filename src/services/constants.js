export function fixMatches(matches) {
  matches.map((m) => fixLeagues(m.league));
  return matches.filter((m) => m.home.id != 190443 && m.away.id != 190443);
}

export function fixLeagues(league) {
  switch (parseInt(league.id)) {
    case 1304:
      league.name = "Liga Nacional";
      break;
    case 7592:
      league.name = "Liga Argentina";
      break;
    case 28862:
      league.name = "Liga Federal";
      break;
    case 1525:
      league.name = "ACB";
      break;
    case 1583:
      league.name = "Lega 1";
      break;
  }
}
